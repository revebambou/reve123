package com.reve.multiThread;

public class Manjyu {
	private String manjyuKawa;
	private String manjyuNiku;
	private boolean manjyuExist;
	/**
	 * @return the manjyuKawa
	 */
	public String getManjyuKawa() {
		return manjyuKawa;
	}
	/**
	 * @param manjyuKawa the manjyuKawa to set
	 */
	public void setManjyuKawa(String manjyuKawa) {
		this.manjyuKawa = manjyuKawa;
	}
	/**
	 * @return the manjyuNiku
	 */
	public String getManjyuNiku() {
		return manjyuNiku;
	}
	/**
	 * @param manjyuNiku the manjyuNiku to set
	 */
	public void setManjyuNiku(String manjyuNiku) {
		this.manjyuNiku = manjyuNiku;
	}
	/**
	 * @return the manjyuExist
	 */
	public boolean isManjyuExist() {
		return manjyuExist;
	}
	/**
	 * @param manjyuExist the manjyuExist to set
	 */
	public void setManjyuExist(boolean manjyuExist) {
		this.manjyuExist = manjyuExist;
	}

	
	
}
